function numberCompare(numberA, numberB){
    if (numberA == numberB){
        alert(numberA + ' and ' + numberB + ' are equal');
    }
    else if (numberA < numberB){
        alert(numberA + ' is less than ' + numberB);
    }
    else if (numberA <= numberB){
        alert(numberA + ' is less than or equal to' + numberB);
    }
    else if (numberA > numberB){
        alert(numberA + ' is greater than ' + numberB);
    }
    else if (numberA >= numberB){
        alert(numberA + ' is greater than or equal to ' + numberB);
    }
}
